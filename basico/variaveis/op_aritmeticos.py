# Introdução aos operadores aritmeticos 
adicao = 10+10
print('Adicao:', adicao)

subtracao = 10-5
print('Subtracao:', subtracao)

divisao = 10/2.2 
print('Divisao:', divisao) #retorna valor em float sempre

divisao_inteira = 10//2.2 
print('Divisao:', divisao_inteira) #retorna valor em int, e caso seja usado valor float na operação, o resultado é arredondado

exponenciacao = 10 ** 2
print('Exponenciacao:', exponenciacao)

modulo=55%2
print('Modulo:', modulo) #resto da divisão

conta_1 = 1 + 1 ** 5 + 5 # resultado 7
print(conta_1)
