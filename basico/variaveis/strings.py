# Print básico, usando separador e quebra de linha
print(12, 34, sep='-', end='#')
print(56, 78, sep="-", end='\n')
print(9, 10, sep='-', end='\n')

# Print de string com aspas simples e duplas
print('Marcos')
print("Joao")
print('Alcino "Junior"')

# Escape
print("Luiz \"Otavio\"")

# r
print(r"Luiz \"Otavio\"")

# Python é uma linguagem de Tipagem dinâmica e forte, pois permite que você defina variáveis sem especificar o tipo de dado (tipagem dinâmica) e garante que os dados mantêm seu tipo sem conversões automáticas entre tipos diferentes (tipagem forte)