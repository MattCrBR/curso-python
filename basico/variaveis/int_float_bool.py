# Tipo int, int é um número inteiro, positivo ou negativo
print(11) 
print(-11) 
print(0)

# Tipo float, float é um numero com ponto flutuante, positivo ou negativo
print(1.1)
print(10.11, -1.5)
print(0.0)

# A class type mostra o tipo que o Python inferiu ao valor
print(type('Marcos'))
print(type(0))
print(type(1.1), type(-1.1), type(0.0))

# Tipo bool (boolean), só existem duas respostas possiveis: sim (True) ou não (False)
print(10 == 10) # Sim = True
print(10 == 11) # Não = False
print(type(10 == 10))